# Pre-processing
library(dplyr)
library(scda)
library(tern)

# Safety population
adsl <-
  synthetic_cdisc_dataset("rcd_2022_06_27", "adsl") %>%
  df_explicit_na() %>%
  filter(SAFFL == "Y")

# Vital signs - DIABP
advs <-
  synthetic_cdisc_dataset("rcd_2022_06_27", "advs") %>%
  df_explicit_na() %>%
  filter(PARAMCD == "DIABP" & SAFFL == "Y" & AVISITN >= 1) %>%
  group_by(USUBJID, PARAMCD) %>%
  mutate(maxDIABP = max(AVAL)) %>%
  ungroup() %>%
  mutate(
    L60 = (maxDIABP < 60),
    U60 = (maxDIABP > 60),
    U90 = (maxDIABP > 90),
    U110 = (maxDIABP > 110),
    UE120 = (maxDIABP >= 120)
  ) %>%
  var_relabel(
    L60 = "<60",
    U60 = ">60",
    U90 = ">90",
    U110 = ">110",
    UE120 = ">=120"
  )

# Build layout
lyt <-
  basic_table(show_colcounts = TRUE) %>%
  split_cols_by("ARM") %>%
  count_patients_with_flags(
    var = "USUBJID",
    flag_variables = var_labels(advs[, c("L60", "U60", "U90", "U110", "UE120")])
  )


# Build table
result <- build_table(lyt, df = advs, alt_counts_df = adsl)

# Add titles/footnotes

## Top left header
top_left(result) <- "Diastolic Blood Pressure\n(Pa)"

## Title
main_title(result) <-
  "Table 32. Percentage of Patients With Maximum Diastolic Blood Pressure by Category of Blood
Pressure Postbaseline, Safety Population, Pooled Analysis"

## Footnotes
main_footer(result) <- c(
  "Source: [include Applicant source, datasets and/or software tools used].",
  "Abbreviations: CI, confidence interval; n, number of patients with indicated blood pressure;
  N, number of patients in treatment arm with available blood pressure data"
)

# Print result
result
